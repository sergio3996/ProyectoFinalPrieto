class Product{
    
    id;
    name;
    category;
    description;
    price;
    imgURL;

    constructor(id, name, category, description, price, imgURL){
        this.id = id;
        this.name = name;
        this.category = category;
        this.description = description;
        this.price = price;
        this.imgURL = imgURL;

    }

}
