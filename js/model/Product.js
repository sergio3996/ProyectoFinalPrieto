class Product{
    
    id;
    name;
    description;
    category;
    price;
    imgURL;

    constructor(id, name, description, category, cost, image){
        this.id = id;
        this.name = name;
        this.description = description;
        this.category = category;
        this.price = cost;
        this.imgURL = image;

    }

}
